#ifndef MODEMANAGER_H
#define MODEMANAGER_H

namespace DavinciEngine{

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary> Manager for the different engine modes. </summary>
	////////////////////////////////////////////////////////////////////////////////////////////////////
	class ModeManager
	{
	private:

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary> Default constructor.</summary>
		////////////////////////////////////////////////////////////////////////////////////////////////////
		ModeManager();

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary> Destructor.</summary>
		////////////////////////////////////////////////////////////////////////////////////////////////////
		~ModeManager();

		/// <summary> Pointer to the singleton ModeManager object </summary>
		static ModeManager *m_ModeManager;

		/// <summary> The current game mode </summary>
		static int gameMode;

	public:

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary> Gets the instance.</summary>
		///
		/// <returns> null if it fails, else the instance.</returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////
		static ModeManager *GetInstance();

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary> Destroys this object.</summary>
		////////////////////////////////////////////////////////////////////////////////////////////////////
		void Destroy();

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary> Sets the current engine mode.</summary>
		///
		/// <param name="mode"> The mode.</param>
		////////////////////////////////////////////////////////////////////////////////////////////////////
		void SetMode(int mode) {gameMode = mode;}

		////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary> Gets the current engine mode.</summary>
		///
		/// <returns> The mode.</returns>
		////////////////////////////////////////////////////////////////////////////////////////////////////
		int GetMode() {return gameMode;}
	};
};

#endif